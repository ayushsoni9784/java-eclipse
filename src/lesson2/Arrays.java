package lesson2;
// Arrays 
public class Arrays {

	public static void main(String[] args) {
		int[] myintarray = {1,2,3,4,5};
		// int myintarray1[] = {1,2,3,4,5};
		System.out.println(myintarray[2]);
		int index = 0;
		while(index < 5) {
			System.out.println(myintarray[index]);
			index++;
		}
	}

}
